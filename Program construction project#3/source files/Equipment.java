public class Equipment {
    public String name;
    public int price;
    public int attack;
    public int defence;
    public int health;
    public int speed;

    public Equipment(String name,int price,int attack,int defnce, int health,int speed){
        this.name = name;
        this.price = price;
        this.attack = attack;
        this.defence = defnce;
        this.health = health;
        this.speed = speed;
    }
}
